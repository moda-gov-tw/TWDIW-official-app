#!/bin/bash

# ===================================================================
# 數位發展部數位憑證皮夾驗證端管理服務 - 啟動腳本
# ===================================================================
# 此腳本會載入 .env 檔案中的環境變數並啟動 Spring Boot 應用
# 使用方式：./start.sh
# ===================================================================

# 設定腳本錯誤時立即退出
set -e

# 顯示當前目錄
echo "當前工作目錄: $(pwd)"

# 檢查 .env 檔案是否存在
if [ ! -f .env ]; then
    echo "錯誤：找不到 .env 檔案！"
    echo "請先建立 .env 檔案並設定必要的環境變數。"
    exit 1
fi

echo "載入 .env 檔案中的環境變數..."

# 載入 .env 檔案（跳過註解行和空行）
export $(grep -v '^#' .env | grep -v '^$' | xargs)

echo "環境變數載入完成！"
echo ""
echo "====================================================================="
echo "啟動 Spring Boot 應用..."
echo "====================================================================="
echo ""

# 啟動 Spring Boot 應用
./mvnw spring-boot:run
